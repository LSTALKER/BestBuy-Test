package com.bestbuy.android.data.actionapi;

public class AApiTarget {

	private String firstName;
	private String lastName;
	private String atgId;
	private String rewardZone;
	private String id;
	private String phone;
	private String address;
	private String email;
	
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getAtgId() {
		return atgId;
	}
	public void setAtgId(String atgId) {
		this.atgId = atgId;
	}
	public String getRewardZone() {
		return rewardZone;
	}
	public void setRewardZone(String rewardZone) {
		this.rewardZone = rewardZone;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}
	
	
	
	
}
